<!-- HTML
<html> — корневой элемент HTML-документа.
<head> — содержит метаданные документа, такие как заголовок и ссылки на стили или скрипты.
<meta> — мета-информация (например, кодировка, настройка для мобильных устройств).
<title> — заголовок страницы, который виден во вкладке браузера.
<link> — подключает внешний файл стилей CSS.
<body> — основной контент страницы.
<header> — содержит заголовок и основную информацию о странице.
<nav> — используется для навигации по сайту.
<main> — основной контент страницы.
<section> — логическая секция контента.
<aside> — боковая информация или дополнения к основному контенту.
<footer> — подвал страницы, обычно содержит копирайты или контактную информацию.
<script> — подключает JavaScript.
    -->